public class Operators {
    public static void main(String[] args)
    {
        int a=10;
        int b=20;
        System.out.println("sum ="+(a+b));
        System.out.println("diff ="+(a-b));
        System.out.println("product ="+(a*b));
        System.out.println("division ="+(a%b));

    }
}
