public class Whileloops{
    public static void main(String[] args) {
        int i=0;
        while(i<=5){ 
            //executes untill the condition is true....

            System.out.println("hello");
            i++;
        }
        do { 
            System.out.println("hii");
            i++;
        } // execute do block untill while condition goes true ... ... ...
        while (i<=6);
    }
}